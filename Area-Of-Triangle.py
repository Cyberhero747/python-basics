# Input the base and height of the triangle from the user. Formula for Area of Triangle is '(base x height)/2' or '(base x height) x 0.5
Base = float(input("Enter the length of the base of the triangle: "))
Height = float(input("Enter the height of the triangle: "))
Area = 0.5 * Base * Height       # Here, there are Three variables, 'base' and 'height' and 'Area'.
print ("Area of triangle is: ", Area)
